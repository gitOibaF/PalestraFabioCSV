    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        codice = jTextField4.getText().trim();
            
        if(codice != null && !codice.isEmpty()){
   
            jLabel6.setVisible(false);
            listaAbb=PC.search(codice);

            if(listaAbb != null && !listaAbb.isEmpty()){
              jLabel4.setText("Risultato: La persona è stata trovata nell'elenco degli iscritti");
              StringBuilder testoAbbonamenti = new StringBuilder("<html>");
              for (Abbonamenti abbonamento : listaAbb) {
                testoAbbonamenti.append(abbonamento.toString()).append("<br>");
              }
                testoAbbonamenti.append("</html>");

                jLabel3.setText(testoAbbonamenti.toString());
            }
            else if(listaAbb.isEmpty()){
              jLabel4.setText("Risultato: La persona è stata trovata nell'elenco degli iscritti ma non ha alcun abbonamento associato");
            }
            else{
              jLabel4.setText("Risultato: La persona non è stata trovata nell'elenco degli iscritti");
            }

          
        }
        else{
          jLabel6.setVisible(true); 
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

